function getPointsForFinish(tournamentType: string, entrants: number, position: number | string): number {
  const tables = POINTS_TABLES[tournamentType]
  if (!tables) return 0

  let bracket: BracketKey
  if (entrants <= 8) bracket = "1-8"
  else if (entrants <= 16) bracket = "9-16"
  else if (entrants <= 24) bracket = "17-24"
  else if (entrants <= 32) bracket = "25-32"
  else if (entrants <= 64) bracket = "33-64"
  else if (entrants <= 128) bracket = "65-128"
  else return 0

  const table = tables[bracket]
  if (!table) return 0

  const key = String(position) as PositionKey

  return table[key] ?? 0
}

function calculateNewAveragedPoints(currentTotal: number, currentExposures: number, newPoints: number): number {
  const newTotal = currentTotal + newPoints
  const newExposures = currentExposures + 1
  const newDivisor = newExposures <= 4 ? 4 : 4 + Math.floor((newExposures - 4) / 2)
  return Math.round(newTotal / newDivisor)
}



